package ca.mcmaster.se2aa4.island.teamXXX;

public class XYCoordinates{
    private int X;
    private int Y;
    public XYCoordinates(int X, int Y){
        this.X = X;
        this.Y = Y;
    }

    public int getX(){
        return this.X;
    }

    public int getY(){
        return this.Y;
    }

    public void setX(int X){
        this.X = X;
    }

    public void setY(int Y){
        this.Y = Y;
    }
}
